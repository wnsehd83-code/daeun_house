/** 매년 동일한 양력 공휴일 */
const KR_FIXED_SOLAR = {
  "01-01": "신정",
  "03-01": "삼일절",
  "05-05": "어린이날",
  "06-06": "현충일",
  "08-15": "광복절",
  "10-03": "개천절",
  "10-09": "한글날",
  "12-25": "성탄절",
};

function getKrHoliday(dateKey) {
  const [y, m, d] = dateKey.split("-");
  const md = `${m}-${d}`;
  const yearTable = KR_HOLIDAY_DATES[Number(y)];
  if (yearTable?.[md]) return yearTable[md];
  if (KR_FIXED_SOLAR[md]) return KR_FIXED_SOLAR[md];
  return null;
}

function getKrHolidayShort(name) {
  if (!name) return "";
  if (name.length <= 4) return name;
  if (name.startsWith("설날")) return "설";
  if (name.startsWith("추석")) return "추석";
  if (name.includes("대체")) return "대체";
  if (name.includes("부처님")) return "석가";
  return name.slice(0, 3);
}
